"""
Module: talos.projects.flow_cli

Purpose:
    Universal flow CLI — inspect and export any replay flow regardless of
    which module generated it (Input Validation, BAC, manual replay, etc.).

    Every replay flow is self-describing via the flow_meta JSON column
    introduced in schema v27.  This CLI exposes that data.

    Commands:
        talos flow show <flow_id>        — display full request/response + meta
        talos flow export <flow_id>      — write Markdown file to project exports/
        talos flow export --module iv    — export all IV flows
        talos flow export --param <uuid> — export all flows for a parameter UUID

Dependencies: argparse, json, sqlite3, sys
Data flow:
    CLI args → DB reads (flows + flow_meta) → stdout or export file
Side effects:
    - Read-only DB access.
    - export command writes a Markdown file to <project_dir>/exports/.
    - Exits 1 on error.
"""

import argparse
import json
import sqlite3
import sys
from datetime import datetime, timezone
from pathlib import Path

from talos.projects.manager import ProjectManager


def _require_active(manager: ProjectManager):
    project = manager.active()
    if project is None:
        print("Error: no active project. Run 'talos project open <id>' first.",
              file=sys.stderr)
        sys.exit(1)
    return project


def run_flow_cli(manager: ProjectManager, argv: list[str]) -> None:
    """
    Purpose:
        Parse flow subcommand arguments and dispatch to handler.
    Input:
        manager — ProjectManager instance.
        argv    — argument list after 'flow'.
    Side effects:
        Dispatches to command handlers.  Exits 0 for --help, 1 for errors.
    """
    parser = argparse.ArgumentParser(
        prog="talos flow",
        description=(
            "Universal flow inspector.  Works for any replay flow regardless "
            "of which module generated it (IV, BAC, manual replay, etc.)."
        ),
    )
    sub = parser.add_subparsers(dest="flow_cmd", metavar="<command>")
    sub.required = True

    # ------------------------------------------------------------------
    # show
    # ------------------------------------------------------------------
    p_show = sub.add_parser("show", help="Display a replay flow (request + response + metadata).")
    p_show.add_argument("flow_id", help="UUID of the replay flow.")

    # ------------------------------------------------------------------
    # export
    # ------------------------------------------------------------------
    p_export = sub.add_parser(
        "export",
        help="Export one or more replay flows to Markdown.",
    )
    scope_grp = p_export.add_mutually_exclusive_group()
    scope_grp.add_argument("flow_id", nargs="?", help="Single flow UUID to export.")
    scope_grp.add_argument(
        "--module",
        metavar="MODULE",
        help="Export all flows generated by a module (e.g. input_validation, bac).",
    )
    scope_grp.add_argument(
        "--parameter",
        metavar="PARAM_UUID",
        help="Export all IV flows for a parameter UUID.",
    )
    scope_grp.add_argument(
        "--endpoint",
        metavar="ENDPOINT_ID",
        help="Export all flows for an endpoint UUID.",
    )
    scope_grp.add_argument(
        "--flows",
        nargs="+",
        metavar="FLOW_ID",
        help="Export a list of flow UUIDs.",
    )

    args = parser.parse_args(argv)

    if args.flow_cmd == "show":
        _cmd_show(manager, args.flow_id)
    elif args.flow_cmd == "export":
        _cmd_export(manager, args)
    else:
        parser.print_help()
        sys.exit(1)


# ---------------------------------------------------------------------------
# Command handlers
# ---------------------------------------------------------------------------

def _cmd_show(manager: ProjectManager, flow_id: str) -> None:
    """
    Purpose:
        Display a single replay flow with full request, response, and metadata.
    Side effects: Reads DB; prints to stdout.
    """
    project = _require_active(manager)
    flow = _fetch_flow(project.db_path, flow_id)
    if flow is None:
        print(f"Flow '{flow_id}' not found.", file=sys.stderr)
        sys.exit(1)
    print(_render_flow_text(flow))


def _cmd_export(manager: ProjectManager, args: argparse.Namespace) -> None:
    """
    Purpose:
        Export one or more flows to a Markdown file in <project_dir>/exports/.
    Side effects: Reads DB; writes Markdown file.
    """
    project = _require_active(manager)
    db_path = project.db_path

    flow_ids: list[str] = []

    if getattr(args, "flow_id", None):
        flow_ids = [args.flow_id]
    elif getattr(args, "flows", None):
        flow_ids = args.flows
    elif getattr(args, "module", None):
        flow_ids = _fetch_flow_ids_by_module(db_path, args.module)
    elif getattr(args, "parameter", None):
        flow_ids = _fetch_flow_ids_by_param_uuid(db_path, args.parameter)
    elif getattr(args, "endpoint", None):
        flow_ids = _fetch_flow_ids_by_endpoint(db_path, args.endpoint)
    else:
        print("Error: specify a flow_id, --module, --parameter, --endpoint, or --flows.",
              file=sys.stderr)
        sys.exit(1)

    if not flow_ids:
        print("No flows found matching the specified scope.")
        return

    lines: list[str] = [f"# Flow Export — {len(flow_ids)} flows", ""]
    for fid in flow_ids:
        flow = _fetch_flow(db_path, fid)
        if flow is None:
            lines.append(f"## Flow `{fid[:8]}…`")
            lines.append(f"*(not found)*")
            lines.append("")
            continue
        lines.append(_render_flow_markdown(flow))
        lines.append("")

    md_content = "\n".join(lines)
    export_dir = _get_export_dir(project)
    # Build a descriptive filename.
    if len(flow_ids) == 1:
        fname = f"flow_{flow_ids[0][:16]}.md"
    elif getattr(args, "module", None):
        fname = f"flows_{args.module}.md"
    elif getattr(args, "parameter", None):
        fname = f"flows_param_{args.parameter[:16]}.md"
    elif getattr(args, "endpoint", None):
        fname = f"flows_endpoint_{args.endpoint[:16]}.md"
    else:
        fname = f"flows_export_{datetime.now(timezone.utc).strftime('%Y%m%d_%H%M%S')}.md"

    out_path = export_dir / fname
    out_path.write_text(md_content, encoding="utf-8")
    print(f"Exported {len(flow_ids)} flow(s) to {out_path}")


