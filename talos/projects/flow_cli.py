def _fetch_flow(db_path: Path, flow_id: str) -> dict | None:
    """
    Purpose:
        Fetch a single flow row including flow_meta.
        Also fetches replay_reason and original_flow_id for context.
    Side effects: Read-only DB access.
    """
    if not db_path.exists():
        return None
    with sqlite3.connect(str(db_path)) as conn:
        conn.row_factory = sqlite3.Row
        row = conn.execute(
            """
            SELECT id, method, url, host, path, query,
                   request_headers, request_cookies,
                   request_body, status_code,
                   response_headers, response_body, content_type,
                   endpoint_id, role_id, module_id, source,
                   original_flow_id, replay_error, replay_reason,
                   captured_at, response_end,
                   COALESCE(flow_meta, '{}') AS flow_meta
            FROM flows WHERE id = ?
            """,
            (flow_id,),
        ).fetchone()
    return dict(row) if row else None


def _fetch_flow_ids_by_module(db_path: Path, module_name: str) -> list[str]:
    """
    Purpose:
        Fetch flow IDs where flow_meta.generated_by matches module_name.
        Uses source=auto_replay (mechanism) and generated_by (subsystem).
    Side effects: Read-only DB access.
    """
    with sqlite3.connect(str(db_path)) as conn:
        rows = conn.execute(
            """
            SELECT id FROM flows
            WHERE source IN ('auto_replay', 'manual_replay')
              AND COALESCE(flow_meta, '{}') LIKE ?
            ORDER BY captured_at DESC
            """,
            (f'%"generated_by": "{module_name}"%',),
        ).fetchall()
    return [r[0] for r in rows]


def _fetch_flow_ids_by_param_uuid(db_path: Path, param_uuid: str) -> list[str]:
    """
    Purpose:
        Fetch flow IDs linked to a parameter UUID via iv_probe_results.
    Side effects: Read-only DB access.
    """
    with sqlite3.connect(str(db_path)) as conn:
        rows = conn.execute(
            "SELECT flow_id FROM iv_probe_results WHERE param_uuid = ? AND flow_id IS NOT NULL",
            (param_uuid,),
        ).fetchall()
    return [r[0] for r in rows]


def _fetch_flow_ids_by_endpoint(db_path: Path, endpoint_id: str) -> list[str]:
    """
    Purpose:
        Fetch all replay flow IDs for a given endpoint.
    Side effects: Read-only DB access.
    """
    with sqlite3.connect(str(db_path)) as conn:
        rows = conn.execute(
            """
            SELECT id FROM flows
            WHERE endpoint_id = ?
              AND source != 'proxy_capture'
            ORDER BY captured_at DESC
            """,
            (endpoint_id,),
        ).fetchall()
    return [r[0] for r in rows]


def _parse_flow_meta(flow: dict) -> dict:
    """Parse flow_meta JSON column; return empty dict on failure."""
    raw = flow.get("flow_meta") or "{}"
    try:
        return json.loads(raw) if isinstance(raw, str) else (raw or {})
    except (ValueError, TypeError):
        return {}


def _parse_json(value) -> dict:
    """Parse a JSON string to dict; return empty dict on failure."""
    if isinstance(value, dict):
        return value
    try:
        return json.loads(value or "{}")
    except (ValueError, TypeError):
        return {}


def _get_export_dir(project) -> Path:
    """Return (and create) the project's exports directory."""
    export_dir = Path(str(project.db_path).replace("talos.db", "")) / "exports"
    export_dir.mkdir(parents=True, exist_ok=True)
    return export_dir
