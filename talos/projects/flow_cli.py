def _render_flow_text(flow: dict) -> str:
    """
    Purpose:
        Render a flow dict as a human-readable terminal string.
    Output:
        Multi-line string with request, response, and metadata sections.
    Side effects: None.
    """
    lines: list[str] = []
    flow_id = flow.get("id", "")
    method = flow.get("method", "")
    url = flow.get("url", "")
    status_code = flow.get("status_code")
    source = flow.get("source", "")
    replay_reason = flow.get("replay_reason", "")
    original_flow_id = flow.get("original_flow_id", "")
    replay_error = flow.get("replay_error", "")
    captured_at = flow.get("captured_at", "")
    endpoint_id = flow.get("endpoint_id", "")

    lines.append(f"Flow: {flow_id}")
    lines.append("=" * 70)
    lines.append(f"  Source       : {source}")
    lines.append(f"  Reason       : {replay_reason or '(none)'}")
    lines.append(f"  Time         : {captured_at}")
    lines.append(f"  Original     : {original_flow_id or '(none)'}")
    lines.append(f"  Endpoint     : {endpoint_id or '(none)'}")
    if replay_error:
        lines.append(f"  Error        : {replay_error}")
    lines.append("")

    # Flow metadata (generated_by, analysis, payload, etc.)
    meta = _parse_flow_meta(flow)
    if meta:
        lines.append("  Metadata:")
        for k, v in sorted(meta.items()):
            lines.append(f"    {k:<20} : {v}")
        lines.append("")

    # Request
    lines.append("  REQUEST")
    lines.append("  " + "-" * 60)
    lines.append(f"  {method} {url}")
    req_headers = _parse_json(flow.get("request_headers", "{}"))
    for k, v in req_headers.items():
        lines.append(f"  {k}: {v}")
    req_body = flow.get("request_body")
    if req_body:
        if isinstance(req_body, bytes):
            req_body = req_body.decode("utf-8", errors="replace")
        lines.append("")
        lines.append(f"  {req_body[:1024]}")
    lines.append("")

    # Response
    lines.append("  RESPONSE")
    lines.append("  " + "-" * 60)
    lines.append(f"  HTTP/1.1 {status_code or '(no response)'}")
    resp_headers = _parse_json(flow.get("response_headers", "{}"))
    for k, v in list(resp_headers.items())[:20]:
        lines.append(f"  {k}: {v}")
    resp_body = flow.get("response_body")
    if resp_body:
        if isinstance(resp_body, bytes):
            resp_body = resp_body.decode("utf-8", errors="replace")
        lines.append("")
        lines.append(f"  {resp_body[:2048]}")
    lines.append("")

    return "\n".join(lines)


def _render_flow_markdown(flow: dict) -> str:
    """
    Purpose:
        Render a flow dict as a Markdown section for export files.
    Output:
        Markdown string (no trailing newline).
    Side effects: None.
    """
    flow_id = flow.get("id", "")
    method = flow.get("method", "")
    url = flow.get("url", "")
    status_code = flow.get("status_code")
    source = flow.get("source", "")
    replay_reason = flow.get("replay_reason", "")
    original_flow_id = flow.get("original_flow_id", "")
    replay_error = flow.get("replay_error", "")
    captured_at = flow.get("captured_at", "")

    meta = _parse_flow_meta(flow)

    lines: list[str] = []
    lines.append(f"## Flow `{flow_id}`")
    lines.append("")
    lines.append(f"| Field | Value |")
    lines.append(f"|-------|-------|")
    lines.append(f"| Source | {source} |")
    lines.append(f"| Reason | {replay_reason or '—'} |")
    lines.append(f"| Time | {captured_at} |")
    lines.append(f"| Original Flow | `{original_flow_id or '—'}` |")
    lines.append(f"| HTTP Status | **{status_code or '—'}** |")
    if replay_error:
        lines.append(f"| Error | {replay_error} |")
    lines.append("")

    if meta:
        lines.append("### Metadata")
        lines.append("")
        lines.append("| Field | Value |")
        lines.append("|-------|-------|")
        for k, v in sorted(meta.items()):
            lines.append(f"| {k} | `{v}` |")
        lines.append("")

    # Request
    req_headers = _parse_json(flow.get("request_headers", "{}"))
    req_body = flow.get("request_body")
    if isinstance(req_body, bytes):
        req_body = req_body.decode("utf-8", errors="replace")
    req_body = (req_body or "")[:2048]

    lines.append("### Request")
    lines.append("")
    lines.append("```http")
    lines.append(f"{method} {url}")
    for k, v in req_headers.items():
        lines.append(f"{k}: {v}")
    if req_body:
        lines.append("")
        lines.append(req_body)
    lines.append("```")
    lines.append("")

    # Response
    resp_headers = _parse_json(flow.get("response_headers", "{}"))
    resp_body = flow.get("response_body")
    if isinstance(resp_body, bytes):
        resp_body = resp_body.decode("utf-8", errors="replace")
    resp_body = (resp_body or "")[:4096]

    lines.append("### Response")
    lines.append("")
    lines.append("```http")
    lines.append(f"HTTP/1.1 {status_code or '(none)'}")
    for k, v in list(resp_headers.items())[:30]:
        lines.append(f"{k}: {v}")
    if resp_body:
        lines.append("")
        lines.append(resp_body)
    lines.append("```")

    return "\n".join(lines)


